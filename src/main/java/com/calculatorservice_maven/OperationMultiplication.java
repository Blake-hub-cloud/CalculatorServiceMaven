package com.calculatorservice_maven;

public class OperationMultiplication {

    double a = 0d;
    double b = 0d;

    double result = 0d;

    public OperationMultiplication(double a, double b, double result) {
        this.a = a;
        this.b = b;
        this.result = result;
    }

    public double mul(double a, double b){
        return result = a * b;
    }

}
