package com.calculatorservice_maven;

public class OperationDivision {

    double a = 0d;
    double b = 0d;

    double result = 0d;

    public OperationDivision(double a, double b, double result) {
        this.a = a;
        this.b = b;
        this.result = result;
    }

    public double div(double a, double b){
        return result = a / b;
    }

}
