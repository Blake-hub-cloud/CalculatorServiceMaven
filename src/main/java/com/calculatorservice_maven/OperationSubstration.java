package com.calculatorservice_maven;

public class OperationSubstration {

    double a = 0d;
    double b = 0d;

    double result = 0d;

    public OperationSubstration(double a, double b, double result) {
        this.a = a;
        this.b = b;
        this.result = result;
    }

    public double sub(double a, double b){
        return result = a - b;
    }

}
