package com.calculatorservice_maven;

public class OperationSum {

    int a = 0;
    int b = 0;

    int result = 0;

    public OperationSum(int a, int b, int result) {
        this.a = a;
        this.b = b;
        this.result = result;
    }

    public int sum(int a, int b){
        return  result = a + b;
    }

}
